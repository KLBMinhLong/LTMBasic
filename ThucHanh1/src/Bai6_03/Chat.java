    private void startReceiving() {
        running = true;
        receiveThread = new Thread(() -> {
            try {
                socket = new DatagramSocket(0); // Port 0 = tự động chọn port trống
                myPort = socket.getLocalPort(); // Lưu port được gán
                System.out.println("Client đang lắng nghe trên port: " + myPort);
                byte[] receiveData = new byte[1024];
                
                while (running) {
                    DatagramPacket receivePacket = new DatagramPacket(receiveData, receiveData.length);
                    socket.receive(receivePacket);
                    
                    String message = new String(receivePacket.getData(), 0, receivePacket.getLength());
                    
                    // Cập nhật UI từ thread
                    javax.swing.SwingUtilities.invokeLater(() -> {
                        txtChatContent.append(message + "\n");
                    });
                }
            } catch (SocketException e) {
                if (running) {
                    System.err.println("Socket đã đóng");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        });
        receiveThread.start();
    }
    
    private void stopReceiving() {
        running = false;
        if (socket != null && !socket.isClosed()) {
            socket.close();
        }
        if (receiveThread != null) {
            try {
                receiveThread.join(1000);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
    }

