    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String message = txtChat.getText().trim();
        String nick = txtNick.getText().trim();
        String host = txtDomain.getText().trim();

        if (message.isEmpty()) {
            return;
        }

        try {
            // Tạo DatagramSocket để gửi dữ liệu UDP
            DatagramSocket sendSocket = new DatagramSocket();
            
            // Tạo thông điệp theo định dạng "PORT:port|nick: nội dung"
            String fullMessage = "PORT:" + myPort + "|" + nick + ": " + message;
            byte[] sendData = fullMessage.getBytes();
            
            // Lấy địa chỉ IP của server
            InetAddress serverAddress = InetAddress.getByName(host);
            
            // Tạo DatagramPacket để gửi
            DatagramPacket sendPacket = new DatagramPacket(
                sendData, 
                sendData.length, 
                serverAddress, 
                6334
            );
            
            // Gửi packet
            sendSocket.send(sendPacket);
            sendSocket.close();
            
            // Xóa nội dung vừa gửi
            txtChat.setText("");
            
        } catch (Exception e) {
            txtChatContent.append("Lỗi khi gửi: " + e.getMessage() + "\n");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

